#ifndef COLORS_H
#define COLORS_H

// Reset
#define RESET   "\033[0m"

// Foreground (Text) Colors
#define BLACK   "\033[0;30m"
#define RED     "\033[0;31m"
#define GREEN   "\033[0;32m"
#define YELLOW  "\033[0;33m"
#define BLUE    "\033[0;34m"
#define MAGENTA "\033[0;35m"
#define CYAN    "\033[0;36m"
#define WHITE   "\033[0;37m"

// Bold Foreground
#define BRED     "\033[1;31m"
#define BGREEN   "\033[1;32m"
#define BYELLOW  "\033[1;33m"
#define BBLUE    "\033[1;34m"
#define BMAGENTA "\033[1;35m"
#define BCYAN    "\033[1;36m"
#define BWHITE   "\033[1;37m"

// Background Colors
#define BG_BLACK   "\033[40m"
#define BG_RED     "\033[41m"
#define BG_GREEN   "\033[42m"
#define BG_YELLOW  "\033[43m"
#define BG_BLUE    "\033[44m"
#define BG_MAGENTA "\033[45m"
#define BG_CYAN    "\033[46m"
#define BG_WHITE   "\033[47m"

// High Intensity Background
#define BG_IBLACK   "\033[0;100m"
#define BG_IRED     "\033[0;101m"
#define BG_IGREEN   "\033[0;102m"
#define BG_IYELLOW  "\033[0;103m"
#define BG_IBLUE    "\033[0;104m"
#define BG_IMAGENTA "\033[0;105m"
#define BG_ICYAN    "\033[0;106m"
#define BG_IWHITE   "\033[0;107m"

#endif
